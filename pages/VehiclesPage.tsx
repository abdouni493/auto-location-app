      edit: 'ÿ™ÿπÿØŸäŸÑ', delete: 'ÿ≠ÿ∞ŸÅ', history: 'ÿßŸÑÿ≥ÿ¨ŸÑ', details: 'ÿßŸÑÿ™ŸÅÿßÿµŸäŸÑ',
      createTitle: 'ŸÖÿ±ŸÉÿ®ÿ© ÿ¨ÿØŸäÿØÿ©', editTitle: 'ÿ™ÿπÿØŸäŸÑ ÿßŸÑŸÖÿ±ŸÉÿ®ÿ©',
      save: 'ÿ≠ŸÅÿ∏ ÿßŸÑŸÖÿ±ŸÉÿ®ÿ©', cancel: 'ÿ•ŸÑÿ∫ÿßÿ°',
      confirmDelete: 'ŸáŸÑ ÿ£ŸÜÿ™ ŸÖÿ™ÿ£ŸÉÿØ ŸÖŸÜ ÿ≠ÿ∞ŸÅ Ÿáÿ∞Ÿá ÿßŸÑŸÖÿ±ŸÉÿ®ÿ©ÿü',
